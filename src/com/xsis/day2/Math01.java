package com.xsis.day2;

public class Math01 {
    public static void main(String[] args) {
        //math absolut, return number absolut
        int m=-4;
        double d=-4.5;
        System.out.println("Math Absolut : "+Math.abs(m));
        System.out.println("Math absolut double : "+Math.abs(d));

        //math pow untuk calculate exponential number
        System.out.println("Math pow : "+Math.pow(2,3));

        //math sqrt untuk akar
        System.out.println("Math sqrt : "+Math.sqrt(4));

        //math rendomize
        System.out.println("Math random : "+Math.random());

    }
}
